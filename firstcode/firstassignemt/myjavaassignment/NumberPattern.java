public class NumberPattern {
	public static void main(String[] args) {
		System.out.println("welcome friend: today is a good day, different pattern....hahaha\n");
		
	
	for(int vertical = 5; vertical >= 1; vertical--) {
			for(int horizontal = vertical; horizontal >= 1; horizontal--) {
				System.out.print(horizontal);
			}
		System.out.println(" ");
	}
}
}